    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String maBS = txtMaBS.getText();
        String maPK = txtMaPK.getText();
        String tenPK = txtTenPK.getText();
        if (!maBS.isEmpty()) {
            if (phongKhamRepository.addPhongKham(new AddPhongKhamRequest(maPK, tenPK, maBS))) {
            JOptionPane.showMessageDialog(this, "Thêm phòng khám thành công");
            }else{
                JOptionPane.showMessageDialog(this, "Có lỗi xảy ra, vui lòng thử lại ...");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Cần chọn bác sĩ phụ trách phòng khám trước khi thêm phòng.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

