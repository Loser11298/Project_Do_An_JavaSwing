    public void showListBacSi() {
        dsBacSi = nhanVienRepository.getAllBacSi();
        txtMaPK.setText("PK"+ Utils.getTimeString("ddyyyyHHmmss"));
        DefaultTableModel mode = (DefaultTableModel) tableBN.getModel();
        mode.setRowCount(0);
        if (!dsBacSi.isEmpty()) {
            Object[] row = new Object[5];
            for (int i = 0; i < dsBacSi.size(); i++) {
                row[0] = i;
                row[1] = dsBacSi.get(i).getMaBS();
                row[2] = dsBacSi.get(i).getTenBS();
                row[3] = dsBacSi.get(i).getTrinhDo();
                row[4] = dsBacSi.get(i).getChucVu();
                mode.addRow(row);
            }
        }
    }

