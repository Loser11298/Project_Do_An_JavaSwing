    public void reloadData() {
        new Thread(() -> {
            dsBenhNhans = bnRepository.getAllBenhNhan();
            showListBenhNhan(dsBenhNhans);
        }).start();
    }

    private void showListBenhNhan(List<BenhNhanResponse> dsBenhNhanResponses) {
        DefaultTableModel mode = (DefaultTableModel) tableBenhNhan.getModel();
        mode.setRowCount(0);
        if (!dsBenhNhanResponses.isEmpty()) {
            Object[] row = new Object[11];
            for (int i = 0; i < dsBenhNhanResponses.size(); i++) {
                row[0] = i;
                row[1] = dsBenhNhanResponses.get(i).getMaBN();
                row[2] = dsBenhNhanResponses.get(i).getHoBn();
                row[3] = dsBenhNhanResponses.get(i).getTenBn();
                row[4] = dsBenhNhanResponses.get(i).getGioiTinh();
                row[5] = dsBenhNhanResponses.get(i).getNgaySinh();
                row[6] = dsBenhNhanResponses.get(i).getCmnd();
                row[7] = dsBenhNhanResponses.get(i).getSdt();
                row[8] = dsBenhNhanResponses.get(i).getNgheNghiep();
                row[9] = dsBenhNhanResponses.get(i).getDiaChi();
                row[10] = dsBenhNhanResponses.get(i).getTrangThai();
                mode.addRow(row);
            }
        }
    }

    private void selectBenhNhan(List<BenhNhanResponse> listBenhNhan) {
        idBNSelect = listBenhNhan.get(indexSelect).getId();
        txtMaBn.setText(listBenhNhan.get(indexSelect).getMaBN());
        txtHo.setText(listBenhNhan.get(indexSelect).getHoBn());
        txtTen.setText(listBenhNhan.get(indexSelect).getTenBn());
        txtCmnd.setText(listBenhNhan.get(indexSelect).getCmnd());
        txtDiaChi.setText(listBenhNhan.get(indexSelect).getDiaChi());
        txtSDT.setText(listBenhNhan.get(indexSelect).getSdt());
        if (listBenhNhan.get(indexSelect).getBenhAnResponse() != null) {
            txtMaBA.setText(listBenhNhan.get(indexSelect).getBenhAnResponse().getMaBA());
        } else {
            txtMaBA.setText("");
        }
        txtNgheNghiep.setText(listBenhNhan.get(indexSelect).getNgheNghiep());
        txtNgaySinh.setDate(DateTimeUtils.getDateInString(listBenhNhan.get(indexSelect).getNgaySinh()));
        cbTrangThai.setSelectedItem(listBenhNhan.get(indexSelect).getTrangThai());
        cbGioiTinh.setSelectedItem(listBenhNhan.get(indexSelect).getGioiTinh());
        new Thread(() -> {
            lbImage.setIcon(APIManager.loadImageIcon(listBenhNhan.get(indexSelect).getAvatar(), lbImage.getWidth(), lbImage.getHeight()));
        }).start();
    }

