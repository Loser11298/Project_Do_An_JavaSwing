    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String maBN = txtMaBn.getText();
        if (maBN != null && !maBN.equals("")) {
            int input = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn bệnh nhân này khỏi hệ thống?", "Select an Option...",
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE);
            if (input == 0) {
                if (bnRepository.deleteBenhNhan(maBN)) {
                    reloadData();
                } else {
                    JOptionPane.showMessageDialog(this, "Đã có lỗi xảy ra, vui lòng thử lại");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Cần chọn bệnh nhân để có thể xóa");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

