    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (tableBenhNhan.getSelectedRow() >= 0) {
            UpdateBenhNhanRequest request = new UpdateBenhNhanRequest();
            request.setId(idBNSelect);
            request.setMaBN(txtMaBn.getText());
            request.setHoBn(txtHo.getText());
            request.setTenBn(txtTen.getText());
            request.setGioiTinh(cbGioiTinh.getSelectedItem().toString());
            request.setNgaySinh(DateTimeUtils.formatDate(txtNgaySinh.getDate()));
            request.setCmnd(txtCmnd.getText());
            request.setNgheNghiep(txtNgheNghiep.getText());
            request.setDiaChi(txtDiaChi.getText());
            request.setSdt(txtSDT.getText());
            request.setTrangThai(cbTrangThai.getSelectedItem().toString());
            bnRepository.updateBenhNhan(request, avataFile);
            reloadData();
        } else {
            JOptionPane.showMessageDialog(this, "Chọn bệnh nhân để cập nhật");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void txtKeySearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKeySearchKeyReleased
        String keySearch = txtKeySearch.getText();
        tempList = null;
        if (!keySearch.equals("")) {
            tempList = new ArrayList<>();
            for (int i = 0; i < dsBenhNhans.size(); i++) {
                String sdt = dsBenhNhans.get(i).getSdt();
                String cmnd = dsBenhNhans.get(i).getCmnd();
                String ten = dsBenhNhans.get(i).getTenBn();
                if (sdt.contains(keySearch) || cmnd.contains(keySearch) || ten.contains(keySearch)) {
                    tempList.add(dsBenhNhans.get(i));
                }
            }
            showListBenhNhan(tempList);
        } else {
            showListBenhNhan(dsBenhNhans);
        }
    }//GEN-LAST:event_txtKeySearchKeyReleased

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        JFileChooser file = new JFileChooser();
        file.setDialogTitle("Chọn ảnh");
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg", "png");
        file.addChoosableFileFilter(filter);
        int result = file.showOpenDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            avataFile = file.getSelectedFile();
            lbImage.setIcon(Utils.resizeImageIcon(avataFile.getAbsolutePath(), lbImage));
        } else {
            JOptionPane.showMessageDialog(null, "Chưa chọn được ảnh");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

