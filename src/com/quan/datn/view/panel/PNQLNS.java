    private void txtKeySearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKeySearchKeyReleased
        String keySearch = txtKeySearch.getText().toUpperCase();
        tempList = null;
        if (!keySearch.equals("")) {
            tempList = new ArrayList<>();
            for (int i = 0; i < dsNhanVien.size(); i++) {
                String sdt = dsNhanVien.get(i).getSdt().toUpperCase();
                String cmnd = dsNhanVien.get(i).getCmnd().toUpperCase();
                String ten = dsNhanVien.get(i).getTen().toUpperCase();
                if (sdt.contains(keySearch) || cmnd.contains(keySearch) || ten.contains(keySearch)) {
                    tempList.add(dsNhanVien.get(i));
                }
            }
            showListNhanVien(tempList);
        } else {
            showListNhanVien(dsNhanVien);
        }
    }//GEN-LAST:event_txtKeySearchKeyReleased

