    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        JFileChooser file = new JFileChooser();
        file.setDialogTitle("Chọn ảnh");
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg", "png");
        file.addChoosableFileFilter(filter);
        int result = file.showOpenDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            avataFile = file.getSelectedFile();
            lbImage.setIcon(Utils.resizeImageIcon(avataFile.getAbsolutePath(), lbImage));
        } else {
            JOptionPane.showMessageDialog(null, "Chưa chọn được ảnh");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String maNV = txtMaNV.getText();
        if (maNV.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Cần chọn nhân viên để xóa");
        } else {
            int input = JOptionPane.showConfirmDialog(null, "Bạn có xác nhận xóa nhân viên này khỏi hệ thống không?", "Select an Option...",
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE);
            if (input == 0) {
                if (nhanVienRepository.deleteNhanVien(maNV)) {
                    JOptionPane.showMessageDialog(this, "Xóa nhân viên thành công");
                    dsNhanVien = nhanVienRepository.getAllNhanVien();
                    showListNhanVien(dsNhanVien);
                } else {
                    JOptionPane.showMessageDialog(this, "Đã có lỗi xảy ra, vui lòng thử lại...");
                }
            }
        }

    }//GEN-LAST:event_jButton6ActionPerformed

