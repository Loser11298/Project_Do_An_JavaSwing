    public void reloadData() {
        new Thread(() -> {
            dsNhanVien = nhanVienRepository.getAllNhanVien();
            showListNhanVien(dsNhanVien);
        }).start();
    }

    private void showListNhanVien(List<NhanVien> dsNhanViens) {
        DefaultTableModel mode = (DefaultTableModel) tableNhanVien.getModel();
        mode.setRowCount(0);
        if (!dsNhanViens.isEmpty()) {
            Object[] row = new Object[16];
            for (int i = 0; i < dsNhanViens.size(); i++) {
                row[0] = i;
                row[1] = dsNhanViens.get(i).getMaNV();
                row[2] = dsNhanViens.get(i).getHo();
                row[3] = dsNhanViens.get(i).getTen();
                row[4] = dsNhanViens.get(i).getGioiTinh();
                row[5] = dsNhanViens.get(i).getNgaySinh();
                row[6] = dsNhanViens.get(i).getCmnd();
                row[7] = dsNhanViens.get(i).getTrinhDo();
                row[8] = dsNhanViens.get(i).getViTri();
                row[9] = dsNhanViens.get(i).getChucVu();
                row[10] = dsNhanViens.get(i).getSdt();
                row[11] = dsNhanViens.get(i).getEmail();
                row[12] = dsNhanViens.get(i).getHocHam();
                row[13] = dsNhanViens.get(i).getHocVi();
                row[14] = dsNhanViens.get(i).getDiaChi();
                row[15] = dsNhanViens.get(i).getNgayVaoLam();
                mode.addRow(row);
            }
        }
    }

    private void selectNhanVien(List<NhanVien> dsNhanViens) {
        idNhanVien = dsNhanViens.get(indexSelect).getId();
        txtMaNV.setText(dsNhanViens.get(indexSelect).getMaNV());
        txtHo.setText(dsNhanViens.get(indexSelect).getHo());
        txtTenNV.setText(dsNhanViens.get(indexSelect).getTen());
        txtCMND.setText(dsNhanViens.get(indexSelect).getCmnd());
        txtDiaCHi.setText(dsNhanViens.get(indexSelect).getDiaChi());
        txtSDT.setText(dsNhanViens.get(indexSelect).getSdt());
        txtChucVu.setText(dsNhanViens.get(indexSelect).getChucVu());
        txtHocHam.setText(dsNhanViens.get(indexSelect).getHocHam());
        txtHocVi.setText(dsNhanViens.get(indexSelect).getHocVi());
        txtTrinhDo.setText(dsNhanViens.get(indexSelect).getTrinhDo());
        txtEmail.setText(dsNhanViens.get(indexSelect).getEmail());
        txtNgaySinh.setDate(DateTimeUtils.getDateInString(dsNhanViens.get(indexSelect).getNgaySinh()));
        txtNgayVaoLam.setDate(DateTimeUtils.getDateInString(dsNhanViens.get(indexSelect).getNgayVaoLam()));
        cbViTri.setSelectedItem(dsNhanViens.get(indexSelect).getViTri());
        cbGioiTinh.setSelectedItem(dsNhanViens.get(indexSelect).getGioiTinh());
        new Thread(() -> {
            lbImage.setIcon(APIManager.loadImageIcon(dsNhanViens.get(indexSelect).getAvatar(), lbImage.getWidth(), lbImage.getHeight()));
        }).start();
    }

    private void updateNhanVien(NhanVien nv){
        for(int i = 0 ; i < dsNhanVien.size(); i ++){
            if (dsNhanVien.get(i).getMaNV() == null ? nv.getMaNV() == null : dsNhanVien.get(i).getMaNV().equals(nv.getMaNV())) {
                dsNhanVien.get(i).setNhanVien(nv);
                break;
            }
        }
    }
    
