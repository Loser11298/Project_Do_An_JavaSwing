    private void showListBenhNhanCho() {
        dsBenhNhan = bnRepository.getAllBenhNhanCho();
        if (!dsBenhNhan.isEmpty()) {
            DefaultTableModel mode = (DefaultTableModel) tableBenhNhan.getModel();
            mode.setRowCount(0);
            Object[] row = new Object[7];
            for (int i = 0; i < dsBenhNhan.size(); i++) {
                row[0] = i;
                row[1] = dsBenhNhan.get(i).getMaBN();
                row[2] = dsBenhNhan.get(i).getHoBn();
                row[3] = dsBenhNhan.get(i).getTenBn();
                row[4] = dsBenhNhan.get(i).getGioiTinh();
                row[5] = dsBenhNhan.get(i).getNgaySinh();
                if (dsBenhNhan.get(i).getBenhAnResponse() != null) {
                    row[6] = dsBenhNhan.get(i).getBenhAnResponse().getLyDoKham();
                } else {
                    row[6] = "";
                }
                mode.addRow(row);
            }
        }
    }

