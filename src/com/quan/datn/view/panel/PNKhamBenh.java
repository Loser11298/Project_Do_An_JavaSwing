    private void btnCapThuocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapThuocActionPerformed
        String input;
        if(txtChiPhi.getText() == null || txtChiPhi.getText().equals("")) 
            input = "0"; 
        else 
            input = txtChiPhi.getText(); 
        long chiPhi = Long.parseLong(input);
        if (!txtMaBN.getText().isEmpty() && tableBenhNhan.getSelectedRow() >= 0) {
            if (chiPhi >= 0) {
                BenhAnRequest request = new BenhAnRequest(dsBenhNhan.get(tableBenhNhan.getSelectedRow()).getBenhAnResponse());
                request.setMaBN(txtMaBN.getText());
                request.setCanNang(Float.parseFloat(txtCanNang.getText()));
                request.setNhomMau(cbNhomMau.getSelectedItem().toString());
                request.setNhietDo(Float.parseFloat(txtNhietDo.getText()));
                request.setMach(Integer.parseInt(txtMach.getText()));
                request.setHuyetAp(Integer.parseInt(txtHuyetAp.getText()));
                request.setNhipTho(Integer.parseInt(txtNhipTho.getText()));
                request.setLyDoKham(txtLyDoKham.getText());
                request.setTinhTrangHT(txtTinhTrangHT.getText());
                request.setBenhSu(txtBenhSu.getText());
                request.setChanDoanSoBo(txtChanDoanSoBo.getText());
                request.setYeuCauThem(txtYCThem.getText());
                request.setChanDoanSauCung(txtChanDoanSauCung.getText());
                request.setHuongDieuTri(txtHuongDieuTri.getText());
                request.setChiPhi(chiPhi);
                if (benhAnRepository.updateBenhAn(request) && bnRepository.nhapVien(request.getMaBN())) {
                    CapThuoc.shareInstances.setVisible(true);
                    CapThuoc.shareInstances.init(txtMaBA.getText());
                } else {
                    JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi khi cập nhật bệnh án...");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Chi phí điều trị phải lớn hơn hoặc bằng 0");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn bệnh nhân");
        }
    }//GEN-LAST:event_btnCapThuocActionPerformed

    private void tableBenhNhanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableBenhNhanMouseClicked
        int selectIndex = tableBenhNhan.getSelectedRow();
        txtMaBN.setText(dsBenhNhan.get(selectIndex).getMaBN());
        txtDiaChi.setText(dsBenhNhan.get(selectIndex).getDiaChi());
        txtHoBN.setText(dsBenhNhan.get(selectIndex).getHoBn());
        txtTenBN.setText(dsBenhNhan.get(selectIndex).getTenBn());
        txtNgaySinh.setText(dsBenhNhan.get(selectIndex).getNgaySinh());
        txtGioiTinh.setText(dsBenhNhan.get(selectIndex).getGioiTinh());
        txtSDT.setText(dsBenhNhan.get(selectIndex).getSdt());
        new Thread(() -> {
            lbImage.setIcon(APIManager.loadImageIcon(dsBenhNhan.get(selectIndex).getAvatar(), lbImage.getWidth(), lbImage.getHeight()));
        }).start();
        BenhAnResponse benhAn = dsBenhNhan.get(selectIndex).getBenhAnResponse();
        if (benhAn != null) {
            txtMaBA.setText(benhAn.getMaBA());
            txtYCKham.setText(benhAn.getLyDoKham());
            txtCanNang.setText(benhAn.getCanNang() + "");
            if (benhAn.getNhomMau() == null) {
                cbNhomMau.setSelectedIndex(0);
            } else {
                cbNhomMau.setSelectedItem(benhAn.getNhomMau());
            }
            txtNhietDo.setText(benhAn.getNhietDo() + "");
            txtMach.setText(benhAn.getMach() + "");
            txtHuyetAp.setText(benhAn.getHuyetAp() + "");
            txtNhipTho.setText(benhAn.getNhipTho() + "");
            txtLyDoKham.setText(benhAn.getLyDoKham());
            txtTinhTrangHT.setText(benhAn.getTinhTrangHT());
            txtBenhSu.setText(benhAn.getBenhSu());
            txtChanDoanSoBo.setText(benhAn.getChanDoanSoBo());
            txtYCThem.setText(benhAn.getYeuCauThem());
            txtChanDoanSauCung.setText(benhAn.getChanDoanSauCung());
            txtHuongDieuTri.setText(benhAn.getHuongDieuTri());
        }
    }//GEN-LAST:event_tableBenhNhanMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        long chiPhi = Long.parseLong(txtChiPhi.getText());
        if (txtMaBN.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn bệnh nhân");
        } else if (chiPhi < 0) {
            JOptionPane.showMessageDialog(this, "Chi phí điều trị phải lớn hơn hoặc bằng 0");
        } else {
            BenhAnRequest request = new BenhAnRequest(dsBenhNhan.get(tableBenhNhan.getSelectedRow()).getBenhAnResponse());
            request.setMaBN(txtMaBN.getText());
            request.setCanNang(Float.parseFloat(txtCanNang.getText()));
            request.setNhomMau(cbNhomMau.getSelectedItem().toString());
            if (!txtNhietDo.getText().equals("")) {
                request.setNhietDo(Float.parseFloat(txtNhietDo.getText()));
            }
            if (!txtMach.getText().equals("")) {
                request.setMach(Integer.parseInt(txtMach.getText()));
            }
            if (!txtHuyetAp.getText().equals("")) {
                request.setHuyetAp(Integer.parseInt(txtHuyetAp.getText()));
            }
            if (!txtNhipTho.getText().equals("")) {
                request.setNhipTho(Integer.parseInt(txtNhipTho.getText()));
            }
            request.setLyDoKham(txtLyDoKham.getText());
            request.setTinhTrangHT(txtTinhTrangHT.getText());
            request.setBenhSu(txtBenhSu.getText());
            request.setChanDoanSoBo(txtChanDoanSoBo.getText());
            request.setYeuCauThem(txtYCThem.getText());
            request.setChanDoanSauCung(txtChanDoanSauCung.getText());
            request.setHuongDieuTri(txtHuongDieuTri.getText());
            request.setChiPhi(chiPhi);
            NhapVien.sharedInstances.setVisible(true);
            NhapVien.sharedInstances.setBenhAnRequest(request);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String maBN = txtMaBN.getText();
        if (!maBN.isEmpty()) {
            if (bnRepository.xuatVien(maBN)) {
                JOptionPane.showMessageDialog(this, "Bệnh nhân đã được xuất viện...");
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "Có lỗi xảy ra, vui lòng thử lại...");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Cần chọn bệnh nhân để xóa");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void txtChiPhiKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtChiPhiKeyPressed
        char c = evt.getKeyChar();
        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {
            txtChiPhi.setEditable(true);
        } else {
            if (evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE
                    || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE) {
                txtChiPhi.setEditable(true);
            } else {
                txtChiPhi.setEditable(false);
            }
        }
    }//GEN-LAST:event_txtChiPhiKeyPressed

