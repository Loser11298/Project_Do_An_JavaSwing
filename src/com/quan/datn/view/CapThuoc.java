    private void updateTable() {
        DefaultTableModel mode = (DefaultTableModel) tableDonThuoc.getModel();
        mode.setRowCount(0);
        if (!dsThuoc.isEmpty()) {
            Object[] row = new Object[6];
            for (int i = 0; i < dsThuoc.size(); i++) {
                row[0] = i;
                row[1] = dsThuoc.get(i).getTenThuoc();
                row[2] = dsThuoc.get(i).getSoLuong();
                row[3] = dsThuoc.get(i).getDonGia();
                row[4] = dsThuoc.get(i).getThanhTien();
                row[5] = dsThuoc.get(i).getHuongDan();
                mode.addRow(row);
            }
        }
    }

