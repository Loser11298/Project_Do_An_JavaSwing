    private boolean exportFilePDF() {
        Document document = new Document();
        try {
            BaseFont baseFont = BaseFont.createFont("Fonts/vuArial.ttf", BaseFont.IDENTITY_H, BaseFont.EMBEDDED);
            PdfWriter.getInstance(document, new FileOutputStream("Outputs/Don-Thuoc-" + maBA + "-" + DateTimeUtils.getDateTimeString() + ".pdf"));
            document.open();
            Paragraph header = new Paragraph();
            header.setIndentationLeft(80);
            header.setIndentationRight(80);
            header.setAlignment(Element.ALIGN_CENTER);
            header.setSpacingAfter(15);
            Phrase phrase = new Phrase("ĐƠN THUỐC", new Font(baseFont, 23, 1));
            header.add(phrase);
            document.add(header);

            PdfPTable t = new PdfPTable(6);
            t.setSpacingBefore(25);
            t.setSpacingAfter(25);

            PdfPCell c1 = new PdfPCell(new Phrase("STT"));
            t.addCell(c1);
            PdfPCell c2 = new PdfPCell(new Phrase("Tên Thuốc", new Font(baseFont)));
            t.addCell(c2);
            PdfPCell c3 = new PdfPCell(new Phrase("Số Lượng", new Font(baseFont)));
            t.addCell(c3);
            PdfPCell c4 = new PdfPCell(new Phrase("Đơn Giá", new Font(baseFont)));
            t.addCell(c4);
            PdfPCell c5 = new PdfPCell(new Phrase("Thành Tiền", new Font(baseFont)));
            t.addCell(c5);
            PdfPCell c6 = new PdfPCell(new Phrase("Các Sử Dụng", new Font(baseFont)));
            t.addCell(c6);
            for (int i = 0; i < dsThuoc.size(); i++) {
                t.addCell(i + "");
                Phrase tenThuoc = new Phrase(dsThuoc.get(i).getTenThuoc(), new Font(baseFont));
                t.addCell(tenThuoc);
                t.addCell(dsThuoc.get(i).getSoLuong() + "");
                t.addCell(dsThuoc.get(i).getDonGia() + "");
                t.addCell(dsThuoc.get(i).getThanhTien() + "");
                Phrase huongDan = new Phrase(dsThuoc.get(i).getHuongDan(), new Font(baseFont));
                t.addCell(huongDan);
                t.completeRow();
            }
            document.add(t);
            document.close();
            return true;
        } catch (IOException | DocumentException ex) {
            ex.printStackTrace();
        }
        return false;
    }

