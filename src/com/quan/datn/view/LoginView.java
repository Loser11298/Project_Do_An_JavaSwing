    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
       String phoneNumber = txt_PhoneNumber.getText();
       String password = txt_password.getText();
        if (phoneNumber.equals("") || password.equals("")) {
            JOptionPane.showMessageDialog(this, "Số điện thoại hoặc mật khẩu không được để trống");
        }else{
            phoneNumber = phoneNumber.replaceFirst("0", "+84");
            NhanVien nv = nhanVienRepository.login(phoneNumber, password);
            if (nv != null) {
                 DBManager.sharedInstance.saveSessionLogin(nv);
                JOptionPane.showMessageDialog(this, "Đăng nhập thành công");
                NhanVien dataNv = DBManager.sharedInstance.getSessionLogin();
                JOptionPane.showMessageDialog(this, "Xin chao "+ dataNv.getFullName());
                Main main = Main.shareInstances;
                main.validate();
                main.updateNewData();
                main.setVisible(true);
                 this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Đăng nhập không thành công");
            }
        }



    }//GEN-LAST:event_btnLoginActionPerformed

    private void txt_PhoneNumberKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_PhoneNumberKeyPressed
        int length = txt_PhoneNumber.getText().length();
        char c = evt.getKeyChar();
        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {
            if (length < 10) {
                txt_PhoneNumber.setEditable(true);
            } else {
                txt_PhoneNumber.setEditable(false);
            }
        } else {
            if (evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE
                    || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE) {
                txt_PhoneNumber.setEditable(true);
            } else {
                txt_PhoneNumber.setEditable(false);
            }
        }

    }//GEN-LAST:event_txt_PhoneNumberKeyPressed

