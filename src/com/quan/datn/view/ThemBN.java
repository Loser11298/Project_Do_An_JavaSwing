    private void getListPhongKham() {
        listPhongKham = pkRepository.getAllPhongKham();
        if (listPhongKham != null && listPhongKham.size() > 0) {
            DefaultComboBoxModel model = new DefaultComboBoxModel();
            listPhongKham.forEach((phongKham) -> {
                model.addElement(phongKham.getTenPK());
            });
            txtPhongKham.setModel(model);
            String tenBS = listPhongKham.get(0).getBacSi().getHo() + " "
                    + listPhongKham.get(0).getBacSi().getTen();
            txtMaBacSi.setText(listPhongKham.get(0).getBacSi().getMaNV());
            txtTenBacSi.setText(tenBS);
        } else {
            listPhongKham = new ArrayList<>();
        }
    }

    private BenhAnRequest getInputPhieuKham() {
        String maBA = "BA" + Utils.getTimeString("MddyyyymmHHss");
        String ngayKhamString = DateTimeUtils.formatDate(txtNgayKham.getDate());
        String gioKhamString = Utils.formatTimeString(txtGioKham.getValue().toString());
        String thoiGianKham = ngayKhamString + " " + gioKhamString;
        String lyDoKham = txtYeuCauKham.getText();
        String maPK = listPhongKham.get(txtPhongKham.getSelectedIndex()).getMaPK();
        return new BenhAnRequest(maBA, txtMaBN.getText(), maPK, thoiGianKham, lyDoKham);
    }

    private AddBenhNhanRequest getInputBenhNhan() {
        String maBNString = txtMaBN.getText();
        String hoString = txtHo.getText();
        String tenString = txtTen.getText();
        String cmndString = txtCMND.getText();
        String ngaySinhString = DateTimeUtils.formatDate(txtNgaySinh.getDate());
        String diaChiString = txtDiaChi.getText();
        String sdtString = txtSDT.getText();
        sdtString = sdtString.replaceFirst("0", "+84");
        String ngheNghiepString = txtNgheNghiep.getText();
        String gioitinhString = cbGioiTinh.getSelectedItem().toString();
        if (maBNString.isEmpty() || hoString.isEmpty() || tenString.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn cần nhập đủ Họ tên bệnh nhân");
            return null;
        } else {
            return new AddBenhNhanRequest(maBNString, hoString, tenString, gioitinhString, ngaySinhString, cmndString, ngheNghiepString, diaChiString, sdtString, publicKey, getInputPhieuKham());
        }
    }

