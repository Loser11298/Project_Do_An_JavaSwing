    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            AddBenhNhanRequest request = getInputBenhNhan();
            if (request != null && !publicKey.equals("")) {
                if (cardManager.saveInfoBenhNhan(request) && benhNhanRepository.addBenhNhan(request, avataFile)) {
                    JOptionPane.showMessageDialog(this, "Thêm bệnh nhân thành công...");
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Có lỗi xảy ra, vui lòng thử lại ...");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Thông tin yêu cầu bị thiếu...");
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Đã có lỗi xảy ra");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

