    private void btnNhapVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNhapVienActionPerformed
        int selectIndex = tablePhongBenh.getSelectedRow();
        if (selectIndex >= 0) {
            String maPB = dsPhongBenh.get(selectIndex).getMaPB();
            request.setMaPB(maPB);
            if (benhAnRepository.updateBenhAn(request)) {
                if (benhNhanRepository.nhapVien(request.getMaBN())) {
                    JOptionPane.showMessageDialog(this, "Bệnh nhân đã được nhập viện điều trị...");
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi...");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi khi cập nhật bệnh án...");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Cần chọn phòng cho bệnh nhân");
        }
    }//GEN-LAST:event_btnNhapVienActionPerformed

