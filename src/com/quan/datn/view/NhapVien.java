    public void getListPhongBenh() {
        new Thread(() -> {
            dsPhongBenh = phongBenhRepository.getAllPhongBenh();
            DefaultTableModel mode = (DefaultTableModel) tablePhongBenh.getModel();
            mode.setRowCount(0);
            if (dsPhongBenh != null && dsPhongBenh.size() > 0) {
                Object[] row = new Object[3];
                for (int i = 0; i < dsPhongBenh.size(); i++) {
                    row[0] = i;
                    row[1] = dsPhongBenh.get(i).getMaPB();
                    row[2] = dsPhongBenh.get(i).getTenPB();
                    mode.addRow(row);
                }
            } else {
                dsPhongBenh = new ArrayList<>();
            }
        }).start();
    }

